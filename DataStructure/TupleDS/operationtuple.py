my_tuple1 = (1, 2, 3, 4, 2, 5, 6)
my_tuple2 = (7, 8, 9)

# concatenation
print(my_tuple1 + my_tuple2)

# multiply
print(my_tuple1 * 4)

# IN operator
print(4 in my_tuple1)

# count
print(my_tuple1.count(2))

# index
print(my_tuple1.index(4))

# len
print(len(my_tuple1))

# max and min
print(max(my_tuple1))
print(min(my_tuple1))

